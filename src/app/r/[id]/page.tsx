import { Metadata } from "next";
import Link from "next/link";
import { notFound } from "next/navigation";
import { getShare } from "@/lib/share";
import { Button } from "@/components/ui/button";
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card";
import { Badge } from "@/components/ui/badge";
import { ArrowRight, Clock, Share2 } from "lucide-react";
import ReactMarkdown from "react-markdown";

interface Props {
  params: Promise<{ id: string }>;
}

export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const { id } = await params;
  const share = await getShare(id);

  if (!share) {
    return {
      title: "Result Not Found | AI Marketing Skills",
    };
  }

  return {
    title: `${share.skillName} Result | AI Marketing Skills`,
    description: `View this ${share.skillName} output generated by AI Marketing Skills.`,
    openGraph: {
      title: `${share.skillName} Result`,
      description: `View this ${share.skillName} output generated by AI Marketing Skills.`,
    },
  };
}

